// Declarando variáveis
// Variáveis são espaços na memória do computador que guardam informações
console.log("Digite o nome do seu jogador");

// Declaração de variáveis com o comando let - let é uma palavra reservada do JavaScript que permite declarar variáveis
let nickname = "Gabriel";

// Variável do tipo string (texto) - string é uma sequência de caracteres e concatenação é a junção de duas ou mais strings
console.log("Bem vindo " + nickname + " ao jogo!");

// Uma constante é um espaço na memória do computador que não pode ser alterado
// Declaração de constantes com o comando const - const é uma palavra reservada do JavaScript que permite declarar constantes
const notificacao = "Pokemon Go diz: ";

console.log( notificacao + " tem um novo pokemon na região");
console.log( notificacao + " você foi derrotado por um líder");